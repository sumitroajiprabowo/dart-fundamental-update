/// comparison operators in dart is a special symbol that can be used to compare two values.
/// comparison operators in dart is a symbol that can be used to compare two values.

// Example
void main() {
  // Variable
  int number1 = 10;
  int number2 = 5;

  // Equal
  bool equal = number1 == number2;
  print(equal); // false

  // Not Equal
  bool notEqual = number1 != number2;
  print(notEqual); // true

  // Greater Than
  bool greaterThan = number1 > number2;
  print(greaterThan); // true

  // Less Than
  bool lessThan = number1 < number2;
  print(lessThan); // false

  // Greater Than or Equal To
  bool greaterThanOrEqual = number1 >= number2;
  print(greaterThanOrEqual); // true

  // Less Than or Equal To
  bool lessThanOrEqual = number1 <= number2;
  print(lessThanOrEqual); // false
}