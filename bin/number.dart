/// Number in dart
/// Integer, Double, and num
/// Integer is a number without a decimal point.
/// Double is a number with a decimal point.
/// num is a number that can be either an integer or a double.

void main(){
  // Integer
  int number = 10; // or number = 10.10;
  print(number); // 10

  // Double
  double number2 = 10.10; // or number2 = 10;
  print(number2); // 10.1

  // num
  num number3 = 10; // or number3 = 10.10;
  print(number3); // 10

  // change the value of the variable integer
  int number11 = 10;
  number11 = 20;
  print(number11); // 20

  // change the value of the variable double
  double number12 = 10.10; // double is a number with a decimal point.
  number12 = 20.20;
  print(number12); // 20.2

  // change the value of the variable num
  num number13 = 10;
  number13 = 20.20;
  print(number13); // 20.2


}