/// While Loop in Dart
/// While Loop is a statement that can be used to make a decision in a program.

// Example
void main() {
  // While Loop
  int i = 0;
  while (i < 3) {
    print(i);
    i++;
  }

  print('------------------');

  // While Loop with variable
  int number = 4;
  int i2 = 0;
  while (i2 < number) {
    print(i2);
    i2++;
  }
}