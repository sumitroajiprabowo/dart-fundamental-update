/// Symbol in Dart is a special data type that can store any type of data.
/// Symbol is a data type in dart.
/// Symbol is a collection of any type of data.


// Example
void main() {
  // Variable Symbol
  Symbol name = #Danu; // String
  print(name); // Symbol("Danu")

  // Change value of variable
  name = #Raka;
  print(name); // Symbol("Raka")

  // Change value of variable
  name = #Rafi;
  print(name); // Symbol("Rafi")

  // Change value of variable
  name = #Rafli;
  print(name); // Symbol("Rafli")
}