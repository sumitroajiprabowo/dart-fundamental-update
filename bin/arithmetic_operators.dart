/// arithmetic operators in dart is a special symbol that can be used to perform mathematical operations.
/// arithmetic operators in dart is a symbol that can be used to perform mathematical operations.

// Example
void main() {
  // Variable
  int number1 = 10;
  int number2 = 5;

  // Addition
  int addition = number1 + number2;
  print(addition); // 15

  // Subtraction
  int subtraction = number1 - number2;
  print(subtraction); // 5

  // Multiplication
  int multiplication = number1 * number2;
  print(multiplication); // 50

  // Division
  double division = number1 / number2;
  print(division); // 2.0

  // Division with integer result
  int division2 = number1 ~/ number2;
  print(division2); // 2

  // Modulo
  int modulo = number1 % number2;
  print(modulo); // 0

  // Increment
  number1++;
  print(number1); // 11

  // Decrement
  number2--;
  print(number2); // 4
}