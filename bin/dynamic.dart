/// Dynamic in Dart is a special data type that can store any type of data.
/// Dynamic is a data type in dart.
/// Dynamic is a collection of any type of data.

// Example
void main() {
  // Variable Dynamic
  dynamic name = 'Danu'; // String
  print(name);

  // Change value of variable
  name = 12; // Integer
  print(name);

  // Change value of variable
  name = true; // Boolean
  print(name);

  // Change value of variable
  name = 180.43; // Double
  print(name);
}