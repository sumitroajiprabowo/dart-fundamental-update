/// Do While Loop in Dart
/// Do While Loop is a statement that can be used to make a decision in a program.

// Example
void main() {
  // Do While Loop
  int i = 0;
  do {
    print(i);
    i++;
  } while (i < 3);

  print('------------------');

  // Do While Loop with variable
  int number = 4;
  int i2 = 0;
  do {
    print(i2);
    i2++;
  } while (i2 < number);
}